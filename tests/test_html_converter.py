def test_escaping_blocks_html_injection() -> None:
    from core.tools.builtin.design.defaults import default_section_settings
    from core.tools.builtin.design.html import ir_to_html
    from core.tools.builtin.design.ir import Container, Heading, Page

    page = Page(
        title="<script>alert(1)</script>",
        slug="x",
        containers=[
            Container(
                settings=default_section_settings(),
                children=[Heading(text="<img onerror=1>", level="h1")],
            )
        ],
    )
    html = ir_to_html(page)["index.html"]
    assert "<script>" not in html
    assert "&lt;script&gt;" in html
    assert "<img onerror" not in html
    assert "&lt;img onerror" in html


# ── CanvaAssetSpec refusal ──────────────────────────────────────


def test_canva_spec_raises_canva_unresolved_error() -> None:
    from core.tools.builtin.design.defaults import default_section_settings
    from core.tools.builtin.design.ir import Container, Page

    page = Page(
        title="t",
        slug="s",
        containers=[
            Container(
                settings=default_section_settings(),
                children=[
                    Image(
                        src=CanvaAssetSpec(
                            query="a pretty hero image", design_type="hero_image"
                        ),
                        alt="alt text",
                    )
                ],
            )
        ],
    )
    with pytest.raises(CanvaUnresolvedError, match="PR I"):
        ir_to_html(page)


# ── Tool wrapper ────────────────────────────────────────────────


@pytest.mark.asyncio
async def test_tool_writes_bundle_to_disk(tmp_path: Path) -> None:
    page_dict = yaml.safe_load((FIXTURES / "hero.yaml").read_text())
    out = await html_export_tool.handler(
        {"ir": page_dict, "output_dir": str(tmp_path / "out")},
        ToolContext(),
    )
    assert not out.is_error
    assert (tmp_path / "out" / "index.html").exists()
    assert (tmp_path / "out" / "styles.css").exists()
    assert out.data["container_count"] == 1
    assert out.data["title"] == "Acme Launch"


@pytest.mark.asyncio
async def test_tool_refuses_missing_ir() -> None:
    out = await html_export_tool.handler(
        {"output_dir": "/tmp/x"}, ToolContext()
    )
    assert out.is_error
    assert "ir" in out.content.lower()


