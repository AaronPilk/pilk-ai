"""Converter tests. Each fixture under tests/fixtures/design/*.yaml is
loaded, rendered, and checked against structural invariants.

We deliberately do NOT compare against golden HTML strings — those
break on every cosmetic formatting change and their "expected" values
are themselves generated by the converter, which makes them
tautological. Instead, each fixture asserts the invariants the spec
cares about: responsive prefixing, accessibility attributes, widget
presence, and shape. A future regression in the converter flips one of
these assertions.
"""

from __future__ import annotations

import re
from pathlib import Path

import pytest
import yaml

from core.tools.builtin.design.html import CanvaUnresolvedError, ir_to_html
from core.tools.builtin.design.html_export import html_export_tool
from core.tools.builtin.design.ir import CanvaAssetSpec, Image, Page
from core.tools.registry import ToolContext

FIXTURES = Path(__file__).parent / "fixtures" / "design"


def _load(name: str) -> Page:
    data = yaml.safe_load((FIXTURES / f"{name}.yaml").read_text())
    return Page.model_validate(data)


def _render(name: str) -> tuple[str, str]:
    bundle = ir_to_html(_load(name))
    return bundle["index.html"], bundle["styles.css"]


# ── Structural invariants (document-wide) ───────────────────────


def test_document_has_doctype_and_viewport() -> None:
    html, _ = _render("hero")
    assert html.startswith("<!DOCTYPE html>")
    assert 'name="viewport"' in html
    assert "tailwindcss.com" in html  # Tailwind CDN script


def test_title_reflects_page_title() -> None:
    html, _ = _render("hero")
    assert "<title>Acme Launch</title>" in html


def test_lang_attribute_applied() -> None:
    html, _ = _render("hero")
    assert '<html lang="en">' in html


def test_styles_css_is_minimal() -> None:
    _, css = _render("hero")
    # Enough to be useful, short enough that big CSS means someone's
    # bypassed the Tailwind-utility convention.
    assert len(css) < 400
    assert "scroll-behavior" in css


